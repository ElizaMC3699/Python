# Booleans

print(type(True))
print(type(False))

# Comparatives Operators

print(5 > 10) # 
print(5 < 10) # 
print(5 >= 10)
print(5 <= 10)
print(5 == 10)
print(5 != 10)

# Logical operators

print(5 > 4 and 6 > 5)
print(5 < 4 and 6 > 5)
print(5 < 4 or 6 > 5)
print(5 > 4 and not 6 < 5)

print(1 or 0)
print(1 and 0)
print(1 and 1)