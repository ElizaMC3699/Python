# Integers

print(type(1))

# Floats

print(type(2.0))

# Change type string to integer

string_num = '5'
int_string_num = int(string_num)
print(int_string_num)
print(type(int_string_num))

# Change type string to float

string_num = '5.0'
float_string_num = float(string_num)
print(float_string_num)
print(type(float_string_num))

# Operators

num_1 = 3
num_2 = 2

print(num_1 + num_2) # addition
print(num_1 - num_2) # subtraction
print(num_1 * num_2) # multiplication
print(num_1 / num_2) # division
print(num_1 ** num_2) # exponential
print(num_1 % num_2) # modulus
print(10 // 3) # floor division operator
